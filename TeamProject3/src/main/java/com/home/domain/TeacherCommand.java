package com.home.domain;

public class TeacherCommand {
	String mem_id, teach_name, teach_career, teach_img, teach_intro;

	public String getMem_id() {
		return mem_id;
	}

	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	public String getTeach_name() {
		return teach_name;
	}

	public void setTeach_name(String teach_name) {
		this.teach_name = teach_name;
	}

	public String getTeach_career() {
		return teach_career;
	}

	public void setTeach_career(String teach_career) {
		this.teach_career = teach_career;
	}

	public String getTeach_img() {
		return teach_img;
	}

	public void setTeach_img(String teach_img) {
		this.teach_img = teach_img;
	}

	public String getTeach_intro() {
		return teach_intro;
	}

	public void setTeach_intro(String teach_intro) {
		this.teach_intro = teach_intro;
	}
}